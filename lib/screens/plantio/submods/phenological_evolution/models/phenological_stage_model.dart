  /// Converter para Map
  Map<String, dynamic> toMap() {
    return {
      'codigo': codigo,
      'nome': nome,
      'descricao': descricao,
      'cultura': cultura,
      'categoria': categoria.toString(),
      'codigoBBCH': codigoBBCH,
      'daeMinimo': daeMinimo,
      'daeMaximo': daeMaximo,
      'alturaMinimaCm': alturaMinimaCm,
      'alturaMaximaCm': alturaMaximaCm,
      'numeroFolhasMinimo': numeroFolhasMinimo,
      'numeroFolhasMaximo': numeroFolhasMaximo,
      'cor': cor.value,
      'recomendacoes': recomendacoes.join('|'),
    };
  }

  /// Criar a partir de Map
  factory PhenologicalStageModel.fromMap(Map<String, dynamic> map) {
    return PhenologicalStageModel(
      codigo: map['codigo'] as String,
      nome: map['nome'] as String,
      descricao: map['descricao'] as String,
      cultura: map['cultura'] as String,
      categoria: _parseCategory(map['categoria'] as String),
      codigoBBCH: map['codigoBBCH'] as int,
      daeMinimo: map['daeMinimo'] as int?,
      daeMaximo: map['daeMaximo'] as int?,
      alturaMinimaCm: map['alturaMinimaCm'] as double?,
      alturaMaximaCm: map['alturaMaximaCm'] as double?,
      numeroFolhasMinimo: map['numeroFolhasMinimo'] as int?,
      numeroFolhasMaximo: map['numeroFolhasMaximo'] as int?,
      cor: Color(map['cor'] as int),
      recomendacoes: (map['recomendacoes'] as String?)?.split('|').where((s) => s.isNotEmpty).toList() ?? [],
    );
  }

  static StageCategory _parseCategory(String category) {
    if (category.contains('vegetativo')) return StageCategory.vegetativo;
    if (category.contains('reprodutivo')) return StageCategory.reprodutivo;
    return StageCategory.vegetativo;
  }

  @override
  String toString() => 'PhenologicalStageModel($codigo - $nome)';
}

