  final Color cor;
  
  /// √çcone representativo
  final IconData icone;
  
  /// Recomenda√ß√µes agron√¥micas
  final List<String> recomendacoes;

  PhenologicalStageModel({
    required this.codigo,
    required this.nome,
    required this.descricao,
    required this.cultura,
    required this.categoria,
    required this.codigoBBCH,
    this.daeMinimo,
    this.daeMaximo,
    this.alturaMinimaCm,
    this.alturaMaximaCm,
    this.numeroFolhasMinimo,
    this.numeroFolhasMaximo,
    this.cor = Colors.green,
    this.icone = Icons.eco,
    this.recomendacoes = const [],
