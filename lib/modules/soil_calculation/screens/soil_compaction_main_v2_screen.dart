  /// Converte um ID de talh√£o textual (ex: UUID) para um inteiro est√°vel
  /// Usamos os primeiros 8 d√≠gitos hexadecimais como inteiro base 16
  int _talhaoIdToInt(String talhaoId) {
    final hex = talhaoId.replaceAll('-', '');
    final take = hex.length >= 8 ? hex.substring(0, 8) : hex.padRight(8, '0');
    return int.parse(take, radix: 16);
  }

  @override
  void initState() {
    super.initState();
    _carregarDados();
  }

  Future<void> _carregarDados() async {
    setState(() => _isLoading = true);

    try {
      final talhoes = await _talhaoLoader.carregarTalhoes();
      setState(() {
        _talhoes = talhoes;
        _isLoading = false;
      });
    } catch (e) {
      setState(() => _isLoading = false);
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Erro ao carregar dados: $e')),
        );
      }
    }
  }

  Future<void> _carregarPontosTalhao(int talhaoId) async {
    final repository = Provider.of<SoilCompactionPointRepository>(
      context,
      listen: false,
    );
    
    final pontos = await repository.getByTalhao(talhaoId);
    setState(() {
      _pontos = pontos;
    });
  }

  Future<void> _gerarPontosAutomaticos() async {
    if (_talhaoSelecionado == null) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Selecione um talh√£o')),
      );
      return;
    }

    showDialog(
      context: context,
      barrierDismissible: false,
      builder: (context) => const Center(
        child: Card(
          child: Padding(
            padding: EdgeInsets.all(24.0),
            child: Column(
              mainAxisSize: MainAxisSize.min,
              children: [
                CircularProgressIndicator(),
                SizedBox(height: 16),
                Text('Gerando pontos de coleta...'),
              ],
            ),
          ),
        ),
      ),
    );

    try {
      final repository = Provider.of<SoilCompactionPointRepository>(
        context,
        listen: false,
      );

      // Verifica se j√° existem pontos auto-gerados
      final hasPoints = await repository.hasAutoGeneratedPoints(
        _talhaoIdToInt(_talhaoSelecionado!.id),
      );

      if (hasPoints) {
        Navigator.pop(context); // Fecha loading
        
        final confirma = await showDialog<bool>(
          context: context,
          builder: (context) => AlertDialog(
            title: const Text('Pontos Existentes'),
            content: const Text(
              'J√° existem pontos gerados para este talh√£o. Deseja apag√°-los e gerar novos?',
            ),
            actions: [
              TextButton(
                onPressed: () => Navigator.pop(context, false),
                child: const Text('Cancelar'),
              ),
              ElevatedButton(
                onPressed: () => Navigator.pop(context, true),
                style: ElevatedButton.styleFrom(backgroundColor: Colors.red),
                child: const Text('Substituir'),
              ),
            ],
          ),
        );

        if (confirma != true) return;

        // Mostra loading novamente
        if (mounted) {
          showDialog(
            context: context,
            barrierDismissible: false,
            builder: (context) => const Center(
              child: Card(
                child: Padding(
                  padding: EdgeInsets.all(24.0),
                  child: Column(
                    mainAxisSize: MainAxisSize.min,
                    children: [
                      CircularProgressIndicator(),
                      SizedBox(height: 16),
                      Text('Gerando novos pontos...'),
                    ],
                  ),
                ),
              ),
            ),
          );
        }

        // Remove pontos antigos
        await repository.deleteAutoGeneratedPoints(_talhaoIdToInt(_talhaoSelecionado!.id));
      }

      // Coordenadas do talh√£o
      final polygonCoords = _talhaoSelecionado!.points;

      // Gera novos pontos
      final pontos = SoilPointGeneratorService.gerarPontosAutomaticos(
        talhaoId: _talhaoIdToInt(_talhaoSelecionado!.id),
        polygonCoordinates: polygonCoords,
        areaHectares: _talhaoSelecionado!.area,
      );

      // Salva no banco
      await repository.insertMany(pontos);

      // Atualiza lista
      await _carregarPontosTalhao(_talhaoIdToInt(_talhaoSelecionado!.id));

      if (mounted) {
        Navigator.pop(context); // Fecha loading
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('‚úÖ ${pontos.length} pontos gerados com sucesso!'),
            backgroundColor: AppColors.success,
          ),
        );
      }
    } catch (e) {
      if (mounted) {
        Navigator.pop(context); // Fecha loading
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Erro: $e')),
        );
      }
    }
  }

