  Widget _buildNivelRow(String nivel, int quantidade) {
    Color cor;
    switch (nivel) {
      case 'Solto':
        cor = const Color(0xFF4CAF50);
        break;
      case 'Moderado':
        cor = const Color(0xFFFFEB3B);
        break;
      case 'Alto':
        cor = const Color(0xFFFF9800);
        break;
      case 'Cr√≠tico':
        cor = const Color(0xFFF44336);
        break;
      default:
        cor = const Color(0xFF9E9E9E);
    }

    return Padding(
      padding: const EdgeInsets.only(bottom: 8.0),
      child: Row(
        children: [
          Container(
            width: 16,
            height: 16,
            decoration: BoxDecoration(
              color: cor,
              shape: BoxShape.circle,
            ),
          ),
          const SizedBox(width: 8),
          Text('$nivel:'),
          const Spacer(),
          Text(
            '$quantidade ${quantidade == 1 ? "ponto" : "pontos"}',
            style: const TextStyle(fontWeight: FontWeight.bold),
          ),
        ],
      ),
    );
  }

